package javaOperators;

public class Main {
    public static void main(String[] args) {
        Operators operators = new Operators();
        operators.useArtithmeticOperators();
        operators.useLogicalOperator();
        operators.useTypeCasting();
        operators.useAssignmentOperators();
        operators.useUnaryOperators();
        operators.useBitwise();
        operators.useBitwiseForCryptography();
        operators.useTernary();
        operators.useOrderPrecedence();
        operators.useModulus();
        operators.findStringSize();
    }
}
